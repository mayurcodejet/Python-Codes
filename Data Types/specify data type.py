x = str("Hello World")	#str
print(x)

y = int(20)	#int
print(y)

q = float(20.5)	#float
print(q)	

d = complex(1j)	#complex
print(d)

X = list(("apple", "banana", "cherry"))	#list
print(X)

z = tuple(("apple", "banana", "cherry"))	#tuple
print(z)

r = range(6)	#range
print(r)

c = dict(name="John", age=36)	#dict
print(c)

f = set(("apple", "banana", "cherry"))	#set
print(f)	

b = frozenset(("apple", "banana", "cherry"))	#frozenset
print(b)

h = bool(5)	#bool
print(h)

i = bytes(5)	#bytes
print(i)

n = bytearray(5)	#bytearray
print(n)

k = memoryview(bytes(5))  #memory view
